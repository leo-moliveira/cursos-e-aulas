CREATE TABLE IF NOT EXISTS fadergs.Clientes
(
Id INT AUTO_INCREMENT PRIMARY KEY,
NomeCompleto VARCHAR(100) NOT NULL,
DataNascimento DATE NOT NULL,
EstadoCivil SMALLINT NOT NULL,
Sexo VARCHAR(1) NOT NULL,
RG INT UNSIGNED NOT NULL,
CPF VARCHAR(11) NOT NULL,
Pais VARCHAR (30) NOT NULL,
Estado VARCHAR (20) NOT NULL,
Cidade VARCHAR (30) NOT NULL,
Bairro VARCHAR (30) NOT NULL,
CEP VARCHAR(10) NULL,
Logradouro VARCHAR(50) NOT NULL,
Complemento VARCHAR (30) NULL,
Numero SMALLINT UNSIGNED NOT NULL,
TelefoneFixo1 INT UNSIGNED NULL,
TelefoneFixo2 INT UNSIGNED NULL,
Celular1 INT UNSIGNED NOT NULL,
Celular2 INT UNSIGNED NULL,
DataRegistro DATE NOT NULL,
FULLTEXT idx_NomeCompleto (NomeCompleto),
INDEX idx_Id (Id),
INDEX idx_RG (RG)
) ENGINE = InnoDB;